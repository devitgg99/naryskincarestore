// Helper to generate deterministic supplier prices and stocks
export const initialSupplierPrices = [];

// Suppliers list for loop
const suppliersList = initialSuppliers;

// Generates prices around base_price with slight variance per supplier
// Not all products are available from all suppliers. Let's make 75% availability rate.
initialProducts.forEach((product) => {
  let cheapestIndex = Math.floor(Math.random() * suppliersList.length);
  
  suppliersList.forEach((supplier, idx) => {
    // 80% availability
    const isAvailable = (idx === cheapestIndex) || (Math.random() > 0.25);
    if (!isAvailable) return;
    
    // Price variance: within -8% to +10% of base price
    let variance;
    if (idx === cheapestIndex) {
      variance = -0.05 - (Math.random() * 0.05); // cheapest has -5% to -10%
    } else {
      variance = -0.02 + (Math.random() * 0.12);
    }
    const price = Math.round((product.base_price * (1 + variance)) * 100) / 100;
    
    // Stock quantities: e.g. "12 pcs", "2 lo", "10 cs"
    // We store stock_qty as integer in pcs, and stock_unit
    const stockUnits = ["pcs", "lo", "cs"]; // lo = dozen (12 pcs), cs = case (24 pcs)
    const stockUnit = stockUnits[Math.floor(Math.random() * stockUnits.length)];
    let stockQty = Math.floor(Math.random() * 15) + 1; // 1 to 15 cases/dozens/pieces
    
    initialSupplierPrices.push({
      id: `sp_${product.id}_${supplier.id}`,
      product_id: product.id,
      supplier_id: supplier.id,
      price: price,
      stock_qty: stockQty,
      stock_unit: stockUnit,
      updated_at: new Date().toISOString()
    });
  });
});

